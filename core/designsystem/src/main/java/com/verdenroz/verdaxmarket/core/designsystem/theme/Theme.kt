@Composable
fun VxmTheme(
    isDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable (isDarkTheme: Boolean) -> Unit
) {
    MaterialTheme(
        colorScheme = if (isDarkTheme) darkScheme else lightScheme,
        typography = AppTypography,
        content = { content(isDarkTheme) }
    )
}

